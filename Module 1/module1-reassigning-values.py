# reassigning values
age = 28
print(age)

age = 28
new_age = age + 5
print(new_age)

age = 28
age = age + 7
print(age)

age = 28
age += 7
print(age)

# string concatenation 
text = "hokus" + "pokus"
print(text)

# we can also multiply a string by an integer in python
print("hokus" * 5)
# it would be printed 5 times

