# addition
print(2 + 3)

# subtraction
print(5 - 4)

# multiplication
print(3 * 5)

# standard division (will always return a float number)
print(6 / 2)
print(7 / 2)

# integer division (returns the nearest whole number rounded down unless you pair it with a float)
print(6 // 2)
print(7 // 2)

# modulus division (returns the remainder of the division)
print(6 % 2)
print(7 % 2)

# power operator
print( 2 ** 3)

# Note: it you pair an integer with a float you will get a float (ex: 2 + 3.0 = 5.0)